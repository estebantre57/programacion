# SLICING
players = ["cr7" , 'messi' , 'Travis' , 'chicha' , 'corona']
print(players[0:3]) 
# slice es trabajar con un grupo especifico 
# de elementos de una lista
print(players[1:4]) #  ['messi' , 'Travis' , 'chicha']
print(players[:4]) # players = ["cr7" , 'messi' , 'Travis' , 'chicha']
print(players[2:]) 

print(players[-3:])

print(players[-3:-1])